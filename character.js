class Character {
    PhysicalAttack;
    CurrentHealthPoint;
    MaxHealthPoint;
    CurrentMana;
    MaxMana;
    Velocity;
    Armor;
    Level;
    CurrentGold; 

    constructor(physicalAttack, currentHealthPoint, maxHealthPoint, currentMana, maxMana, velocity, armor, level, currentGold) {
        this.PhysicalAttack = physicalAttack;
        this.CurrentHealthPoint = currentHealthPoint;
        this.MaxHealthPoint = maxHealthPoint;
        this.CurrentMana = currentMana;
        this.MaxMana = maxMana;
        this.Velocity = velocity;
        this.Armor = armor;
        this.Level = level;
        this.CurrentGold = currentGold;
    }
}


